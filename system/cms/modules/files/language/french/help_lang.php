<h4>Présentation</h4><hr>
<p>Le module de fichiers permet à l\'administrateur du site de gérer les fichiers utilisés sur son site.
Toutes les images ou les fichiers intégrés dans les pages, galeries ou les billets de blog sont stockés ici.
En ce qui concerne plus particulièrement les images utilisées dans vos contenus de pages, vous pouvez les charger directement à partir de l\'éditeur WYSIWYG ou, alternativement, les charger dans le module de fichiers pour n\'avoir plus qu\'à les insérer ensuite via l'\éditeur WYSIWYG.</p>

<h6>Gestion des dossiers</h6>
<p>Après avoir créé le ou le(s) dossier(s) de premier niveau, vous pouvez créer autant de sous-dossiers que vous le souhaitez, comme, par exemple, blog/images/screenshots/image.jpg et pages/audio/sample.mp3.
L\'intitulé des dossiers n\'est qu\'à votre intention : il n\'est pas affiché dans le lien de téléchargement proposé sur le site public.
Pour éditer un dossier, sélectionnez Gérer les dossiers dans la colonne de gauche puis cliquer sur Editer dans le dossier souhaité.</p>

<h6>Gérer les fichiers</h6>
<p>Pour gérer les fichiers, sélectionnez le nom du dossier dans la colonne de gauche. Si le fichier se trouve dans un sous-dossier, sélectionnez simplement le dossier parent.
Une fois dans le dossier parent, vous pouvez choisir un sous-dossier et filtrer par type de fichier au moyen du menu déroulant en haut de la page.

Une fois dans le bon dossier, vous pouvez charger des fichiers en cliquant sur le bouton correspondant, à côté du menu déroulant Filtres.
Notez qu\il existe deux manières de consulter vos fichiers : Liste et Grille. La liste shows file details while grid will show a thumbnail of the image.</p>

<h6>Uploading Files</h6>
<p>After clicking the Upload button in the desired folder an upload window will appear.
You may add files by either dropping them in the Upload Files box or by clicking in the box and choosing the files from your standard file dialog.
The selected files will display in a list at the bottom of the screen.
You may then either delete unnecessary files from the list or if satisfied click Upload to start the upload process.
If the upload is not successful check your file size. Many hosts do not allow file uploads over 2MB.
Many modern cameras produce images in exess of 5MB.
To remedy this limitation you may either ask your host to change the upload limit or you may wish to resize your images before uploading.
Resizing has the added advantage of faster upload times.</p>";
